public class Facility {  	   // 시설
	int AdministrativeOffice;  // 행정실
	int Staffroom; 			   // 교무실	
	int PrintingRoom;          // 인쇄실
	int ClassRoom;             // 교실   
	int Playground;			   // 운동장
	int Restroom;   	       // 화장실
	
	public int getAdministrativeOffice() {
		return AdministrativeOffice;
	}
	public void setAdministrativeOffice(int administrativeOffice) {
		AdministrativeOffice = administrativeOffice;
	}
	public int getStaffroom() {
		return Staffroom;
	}
	public void setStaffroom(int staffroom) {
		Staffroom = staffroom;
	}
	public int getPrintingRoom() {
		return PrintingRoom;
	}
	public void setPrintingRoom(int printingRoom) {
		PrintingRoom = printingRoom;
	}
	public int getClassRoom() {
		return ClassRoom;
	}
	public void setClassRoom(int classRoom) {
		ClassRoom = classRoom;
	}
	public int getPlayground() {
		return Playground;
	}
	public void setPlayground(int playground) {
		Playground = playground;
	}
	public int getRestroom() {
		return Restroom;
	}
	public void setRestroom(int restroom) {
		Restroom = restroom;
	}
}